        self.client = None
        if settings.GROQ_API_KEY:
            self.client = Groq(api_key=settings.GROQ_API_KEY)
            print("✅ Groq client initialized in service")
        else:
            print("⚠️  WARNING: GROQ_API_KEY not found")

